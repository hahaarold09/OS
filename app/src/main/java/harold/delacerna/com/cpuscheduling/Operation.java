public class Operation {


    public double thirdAT(double b1, double b2) {
        return b1 + b2;
    }

    public double forthAT(double b1, double b2, double b3) {
        return b1 + b2 + b3;
    }

    public double secTT(double b1, double b2) {
        return b1 + b2;
    }

    public double thirdTT(double b1, double b2, double b3) {
        return b1 + b2 + b3;
    }

    public double forthTT(double b1, double b2, double b3, double b4) {
        return b1 + b2 + b3 + b4;
    }
}
